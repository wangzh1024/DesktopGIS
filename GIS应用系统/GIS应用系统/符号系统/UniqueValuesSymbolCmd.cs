            if (m_hookHelper.FocusMap.LayerCount > 0)
            {
                UniqueValuesSymbol symbol = new UniqueValuesSymbol(m_hookHelper);
                symbol.Show(m_hookHelper as System.Windows.Forms.IWin32Window);
            }
